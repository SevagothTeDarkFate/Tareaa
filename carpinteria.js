class Madera {
    constructor(nombre, color, textura, tamano, largo, ancho, grosor) {
        this.nombre = nombre;
        this.color = color;
        this.textura = textura;
        this.tamano = tamano;
        this.largo = largo;
        this.ancho = ancho;
        this.grosor = grosor;
    }
    
    cortar() {
        console.log(`Cortando la madera ${this.nombre} en piezas de ${this.tamano}`);
    }
    
    pintar() {
        console.log(`Pintando la madera ${this.nombre} de color ${this.color}`);
    }
    
    lijar() {
        console.log(`Lijando la madera ${this.nombre} para darle una textura de ${this.textura}`);
    }
    
    barnizar() {
        console.log(`Barnizando la madera ${this.nombre} para protegerla`);
    }
    
    medir() {
        console.log(`Midiendo la madera ${this.nombre}: largo ${this.largo} cm, ancho ${this.ancho} cm, grosor ${this.grosor} cm`);
    }
    
    atornillar() {
        console.log(`Atornillando la madera ${this.nombre}`);
    }
}

